# if
a = 20
b = 30
if a > b:
    print("a is greater than b")
# elif
elif a < b:
    print("b is greater than a")
elif a == b:
    print("a is equal to b")
elif a != b:
        print("a is not equal to b")
elif a >= b:
        print("a is greater than or equal to b")
# else
else :
    print("your condition is failed")